#include<bits/stdc++.h>
using namespace std;
int main()
{
    int t,x1=0,y1=0,z1=0;
    cin>>t;
    while(t--)
    {
        int x,y,z;
        cin>>x>>y>>y;
        x1=x1+x;
        y1=y1+y;
        z1=z1+z;
    }
    if(x1==y1&&y1==z1&&z1==0)
        {
            cout<<"YES"<<endl;
        }
        else
            cout<<"NO"<<endl;
}
